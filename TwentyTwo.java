/**Pronic Number**/

import java.util.*;
public class TwentyTwo {
	
	public static Scanner sc = new Scanner(System.in);
	
	public static void main(String[] args) {
		j.p("Number: ");
		int n = sc.nextInt();
		boolean f = false;
		for (int i = 0; i <= n; i++) {
			if ((i * (i + 1)) == n) {
				f = true;
				break;
			}
		}
		System.out.println("\nPronic number: " + f);
	}
}